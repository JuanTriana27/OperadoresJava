public class OperadoresAsignacion {

    public static void main(String[] args) {

        int var1 = 3;

        //suma
        var1 = var1 +1 ;
        var1 += 1;

        //resta
        var1 = var1 - 1;
        var1 -= 1;

        //multiplicacion
        var1 = var1 * 2;
        var1 *= 2;

        //division
        var1 = var1 / 3;
        var1 /= 3;

        //residuo
        var1 = var1 % 3;
        var1 %= 3;
    }
}
